        private static EntityContext GetEntityContext(int Generation)
        {
            return Generation switch
            {
                1 => EntityContext.Gen1,
                2 => EntityContext.Gen2,
                3 => EntityContext.Gen3,
                4 => EntityContext.Gen4,
                5 => EntityContext.Gen5,
                6 => EntityContext.Gen6,
                7 => EntityContext.Gen7,
                77 => EntityContext.Gen7b,
                8 => EntityContext.Gen8,
                88 => EntityContext.Gen8b,
                888 => EntityContext.Gen8a,
                9 => EntityContext.Gen9,
                99 => EntityContext.Gen9a,
                _ => EntityContext.Gen8
            };
        }

        /// <summary>
        /// Returns the Form Identifier and the Species Name.
        /// </summary>
        /// <param name="Nickname">Nicknamed Pokemon from User</param>
        /// <returns>Formated Nickname Identifier and PKHEX Formated Species Name</returns>
        public static FormIdentifier GetFormIdentifier(int game, string Nickname, bool RequesterIsSumo)
        {
            try
            {
                string FolderName = "";
                string Extension = "";
                string FormFile = "";

                switch (game)
                {
                    case 6:
                        FolderName = "Pokemon6";
                        Extension = "pk6";
                        break;
                    case 7:
                    case 88:
                        FolderName = "Pokemon";
                        Extension = "pk7";
                        break;
                    case 8:
                        FolderName = "Pokemon8";
                        Extension = "pk8";
                        break;
                    case 888:
                        FolderName = "Pokemon8a";
                        Extension = "pa8";
                        break;
                    case 9:
                        FolderName = "Pokemon9";
                        Extension = "pk9";
                        break;
                    default:
                        break;
                }

                bool containsDigitForm = int.TryParse(Nickname[..1], out int _);

                FormInfo? form = GetForm(Nickname);

                if (form is null || (form.Species is Species.Pikachu && form.FormName is "partner" && RequesterIsSumo))
                {
                    return new FormIdentifier()
                    {
                        Valid = false,
                        FormFileName = String.Empty,
                        PK = null,
                        FormID = -1
                    };
                }

                FormFile = $"C:\\{FolderName}\\{form.Species}\\{form.FormName.ToLower()}.{Extension}";

                if (File.Exists(FormFile))
                {
                    PKM? pk = EntityFormat.GetFromBytes(File.ReadAllBytes(FormFile), GetEntityContext(game));
                    if (pk != null)
                    {
                        return new FormIdentifier()
                        {
                            Valid = true,
                            FormFileName = Path.GetFileName(FormFile).Replace($".{Extension}", ""),
                            PK = pk,
                            FormID = pk.Form
                        };
                    }
                }
                return new FormIdentifier()
                {
                    Valid = false,
                    FormFileName = Path.GetFileName(FormFile).Replace($".{Extension}", ""),
                    PK = null,
                    FormID = -1
                };
            }

            catch (Exception ex)
            {
                Console.WriteLine("Exception: " + ex.ToString());
                return new FormIdentifier()
                {
                    Valid = false,
                    FormFileName = String.Empty,
                    PK = null,
                    FormID = -1
                };
            }
        }

        private static FormInfo? GetForm(string form)
        {
            try
            {
                return NicknameForms[form];
            }
            catch { return null; }
        }

